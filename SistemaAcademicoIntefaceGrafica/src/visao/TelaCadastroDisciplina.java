    public static boolean verificarDuplicidade(JComboBox comboBox, Object item){
        for(int i = 0; i < comboBox.getItemCount(); i++ ){
            Object currentItem = comboBox.getItemAt(i);
            if(currentItem.equals(item)){
                return true;
            }
        }
        return false;
    }
    
    @Override
    public void inicializarComponentesTela() {
        jT_Disciplina.setText("");
        jS_CargaHoraria.setValue(2000);
        jS_Semestres.setValue(6);

        ControleDisciplina controleDisciplina = (ControleDisciplina) controle;
        
        for (String nomeCurso : controleDisciplina.getControleCurso().getNomesCursos()) {
            if(!verificarDuplicidade(jCB_Curso, nomeCurso))
            jCB_Curso.addItem(nomeCurso);
        }
        jCB_Curso.setSelectedIndex(0);
    }

    @Override
    public void setarDadosTela(HashMap<String, Object> dados) {
        if (dados == null || dados.isEmpty()) {
            DialogBoxUtils.exibirMensagemDeErro("Erro", "Erro ao setar dados na tela");
        }

        jT_Disciplina.setText((String) dados.getOrDefault("disciplina", ""));
        jS_CargaHoraria.setValue(dados.getOrDefault("cargahoraria", 0));
        jS_Semestres.setValue(dados.getOrDefault("semestre", 0));
        if (dados.get("curso") != null) {
              jCB_Curso.setSelectedItem(((Curso) dados.get("curso")).getNome());
            //jCB_Curso.setSelectedItem(((Docente) dados.get("coordenador")).getNome());
        }
    }

    @Override
    public HashMap<String, Object> getDadosTela() {
        HashMap<String, Object> dados = new HashMap<>();
        dados.put("disciplina", jT_Disciplina.getText());
        dados.put("cargahoraria", jS_CargaHoraria.getValue());
        dados.put("semestre", jS_Semestres.getValue());
        
        //ControleAluno controleAluno = (ControleAluno) controle;
        ControleDisciplina controleDisciplina = (ControleDisciplina) controle;
        int indexCurso = jCB_Curso.getSelectedIndex() - 1;
        Curso cur = controleDisciplina.getControleCurso().getCursoSelecionado(indexCurso);
        
        if (indexCurso >= 0) {
            dados.put("curso", ((ControleDisciplina) controle).getControleCurso().getCursoSelecionado(indexCurso));
        } else {
            dados.put("curso", null);
        }
        return dados;
    }

